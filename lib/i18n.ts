export function t(
  locale: Locale,
  key: string,
  defaultText?: string
): string {
  const keys = key.split(".");
  let value: any = translations[locale];

  for (const k of keys) {
    if (value && typeof value === "object" && k in value) {
      value = value[k];
    } else {
      return defaultText || key;
    }
  }

  return typeof value === "string" ? value : key;
}
